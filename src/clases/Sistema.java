    private void Lista(int opc){
        int Lista[] = new int[30];
        for (int i = 0; i < 30; i++) {
            Lista[i]=1;
        }
        String query = "SELECT * FROM `huespedes`";
        this.conn.Consult(query);
        try {
            do {
                Lista[this.conn.rs.getInt(3)-1]=0;
            } while (this.conn.rs.next());
        } catch (SQLException ex) {
            System.out.println("Error...");
        }
        String Simple="Habitaciones Simples: ",
               Doble="Habitaciones Dobles: ",
               Triple="Habitaciones Triples: ";
        if(opc==0){
            for (int i = 0; i < 15; i++) {
                if(Lista[i]==1){
                    if((i>=0&&i<=3)||(i>=11&&i<=14)){
                        if(i>9)Simple+="0"+(i+1)+", ";
                        else Simple+="00"+(i+1)+", ";
                    }
                    if((i>=4&&i<=5)||(i>=7&&i<=10)){
                        if(i>9)Doble+="0"+(i+1)+", ";
                        else Doble+="00"+(i+1)+", ";
                    }
                    if(i==6){
                        Triple+="00"+(i+1)+", ";
                    }
                }
            }
        }
        else{
            for (int i = 15; i < 30; i++) {
                if(Lista[i]==1){
                    if(i==15||i==29){
                        Simple+="0"+(i+1)+", ";
                    }
                    if((i>=16&&i<=20)||(i>=26&&i<=28)){
                        Doble+="0"+(i+1)+", ";
                    }
                    if(i>=21&&i<=25){
                        Triple+="0"+(i+1)+", ";
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Habitaciones disponibles del Piso "+(opc+1)+": \n"+Simple+"\n"+Doble+"\n"+Triple);
    }
    
    private ArrayList<Huesped> PrepararLista(){
        String query = "SELECT * FROM `huespedes`";
        ArrayList<Huesped> list = new ArrayList<Huesped>();
        this.conn.Consult(query);
        try {
            do {
                persona = new Huesped(
                        this.conn.rs.getString(1),
                        this.conn.rs.getString(2).charAt(0),
                        this.conn.rs.getInt(3),
                        this.conn.rs.getString(4),
                        this.conn.rs.getInt(5),
                        this.conn.rs.getInt(6),
                        this.conn.rs.getDate(7), true);
                list.add(persona);
            } while (this.conn.rs.next());
        } catch (SQLException ex) {
            System.out.println("Error...");
        }
        return list;
    }
